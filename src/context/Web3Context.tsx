const Web3Context = createContext<Web3ContextProps>({
  account: null,
  provider: null,
  signer: null,
  contract: null,
  role: null,
  connect: async () => {},
  disconnect: () => {},
});

export const useWeb3 = () => useContext(Web3Context);

export const Web3Provider: React.FC<{ children: ReactNode }> = ({ children }) => {
  const [account, setAccount] = useState<string | null>(null);
  const [provider, setProvider] = useState<ethers.BrowserProvider | null>(null);
  const [signer, setSigner] = useState<ethers.Signer | null>(null);
  const [contract, setContract] = useState<ethers.Contract | null>(null);
  const [role, setRole] = useState<UserRole>(null);

  // VÃ©rifier si MetaMask est dÃ©jÃ  connectÃ© au chargement
  useEffect(() => {
    const checkConnection = async () => {
      if ((window as any).ethereum) {
        try {
          const ethProvider = new ethers.BrowserProvider((window as any).ethereum);
          const accounts = await ethProvider.listAccounts();
          if (accounts.length > 0) {
            const signer = await ethProvider.getSigner();
            const account = await signer.getAddress();
            setProvider(ethProvider);
            setSigner(signer);
            setAccount(account);
            const contract = new ethers.Contract(CONTRACT_ADDRESS, CONTRACT_ABI, signer);
            setContract(contract);
          }
        } catch (err) {
          console.error('Erreur lors de la vÃ©rification de la connexion:', err);
        }
      }
    };
    checkConnection();
  }, []);

  // Ãcouter les changements de compte MetaMask
  useEffect(() => {
    if ((window as any).ethereum) {
      (window as any).ethereum.on('accountsChanged', async (accounts: string[]) => {
        if (accounts.length === 0) {
          disconnect();
        } else {
          try {
            const ethProvider = new ethers.BrowserProvider((window as any).ethereum);
            const signer = await ethProvider.getSigner();
            const account = await signer.getAddress();
            setProvider(ethProvider);
            setSigner(signer);
            setAccount(account);
            const contract = new ethers.Contract(CONTRACT_ADDRESS, CONTRACT_ABI, signer);
            setContract(contract);
          } catch (err) {
            console.error('Erreur lors du changement de compte:', err);
            disconnect();
          }
        }
      });

      (window as any).ethereum.on('chainChanged', () => {
        window.location.reload();
      });
    }

    return () => {
      if ((window as any).ethereum) {
        (window as any).ethereum.removeAllListeners('accountsChanged');
        (window as any).ethereum.removeAllListeners('chainChanged');
      }
    };
  }, []);

  // Connexion Ã  MetaMask
  const connect = async () => {
    if (!(window as any).ethereum) {
      alert('MetaMask n\'est pas dÃ©tectÃ©. Veuillez installer l\'extension MetaMask.');
      return Promise.reject('MetaMask non dÃ©tectÃ©');
    }
    try {
      const ethProvider = new ethers.BrowserProvider((window as any).ethereum);
      await ethProvider.send('eth_requestAccounts', []);
      const signer = await ethProvider.getSigner();
      const account = await signer.getAddress();
      setProvider(ethProvider);
      setSigner(signer);
      setAccount(account);
      const contract = new ethers.Contract(CONTRACT_ADDRESS, CONTRACT_ABI, signer);
      setContract(contract);
    } catch (err) {
      console.error('Erreur MetaMask:', err);
      alert('Erreur lors de la connexion Ã  MetaMask.');
      return Promise.reject(err);
    }
  };

  // DÃ©connexion
  const disconnect = () => {
    setAccount(null);
    setProvider(null);
    setSigner(null);
    setContract(null);
    setRole(null);
  };

  // DÃ©tection du rÃ´le utilisateur
  useEffect(() => {
    const fetchRole = async () => {
      if (!contract || !account) return setRole(null);
      try {
        const adminRole = await contract.DEFAULT_ADMIN_ROLE();
        const formateurRole = await contract.FORMATEUR_ROLE();
        if (await contract.hasRole(adminRole, account)) setRole('admin');
        else if (await contract.hasRole(formateurRole, account)) setRole('formateur');
        else setRole('apprenant');
      } catch (e) {
        setRole(null);
      }
    };
    fetchRole();
  }, [contract, account]);

  return (
    <Web3Context.Provider value={{ account, provider, signer, contract, role, connect, disconnect }}>
      {children}
    </Web3Context.Provider>
  );
}; 